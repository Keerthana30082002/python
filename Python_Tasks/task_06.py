favorite_fruits=['Apple','Orange','Grapes','Pappaya','Watermelon']
print(favorite_fruits)#printing all the elements of list
print(favorite_fruits[1])#printing second element
print(favorite_fruits[3])#printing fourth element
print(favorite_fruits[:3])#prints first 3 elements of list
#reversed_string=list(reversed(favorite_fruits))
#print(reversed_string)#prints the reverse of list
print(favorite_fruits[::-1])#another way for reversing the list
print(favorite_fruits[-1])#prints the last element of the original list with reverse indexing
print(favorite_fruits[1:4])#prints second to fourth elements of the list
print(favorite_fruits[0::2])#prints the alternate elements of the list
#print(reversed_string[-3:])#prints the last 3 elements of the reversed list
reversed=favorite_fruits[::-1]#reversed list stored in variable reversed
print(reversed[-3:])#prints the last 3 elements of the reversed list

