def add_numbers(num1,num2):
    sum=num1+num2
    print(f'sum of {num1} and {num2} is {sum}')
def calculate_area(length,width):
    area=length*width
    print('Area is ',area)
def print_greeting(name):
    print("Helloo ",name)
def print_power(base,exponent):
    exp=base**exponent
    print(f'{base} to the power {exponent} is {exp}')
def concat_strings(str1,str2):
    str=str1+str2
    print("Concatenation of 2 strings is: ",str)

add_numbers(5,10)
calculate_area(8,4)
print_greeting("Keerthana")
print_power(2,3)
concat_strings("Keerthana","Sivadasan")
